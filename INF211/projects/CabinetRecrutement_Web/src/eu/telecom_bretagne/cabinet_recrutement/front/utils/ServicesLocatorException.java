package eu.telecom_bretagne.cabinet_recrutement.front.utils;

public class ServicesLocatorException extends Exception
{
	//-----------------------------------------------------------------------------
  private static final long serialVersionUID = 7330817454517580864L;
	//-----------------------------------------------------------------------------
	public ServicesLocatorException()
  {
	  super();
  }
	//-----------------------------------------------------------------------------
	public ServicesLocatorException(Throwable cause)
  {
	  super(cause);
  }
	//-----------------------------------------------------------------------------
	public ServicesLocatorException(String message)
  {
	  super(message);
  }
	//-----------------------------------------------------------------------------
	public ServicesLocatorException(String message, Throwable cause)
  {
	  super(message, cause);
  }
	//-----------------------------------------------------------------------------
}
